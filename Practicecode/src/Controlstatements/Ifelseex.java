//If else block 
package Controlstatements;
public class Ifelseex 
{

	public static void main(String[] args) 
	{
		int a=10;
		if(a<=10)
		{
			System.out.println("The value of a is less than 10");// if values is true then execute true block
		}
		else
		{
			System.out.println("The value of a is greater than 10");// if the value is false it execute the false blovk
		}

	}

}
