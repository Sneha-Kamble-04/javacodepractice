package Demo;

public class Logicaloperatex 
{
	public static void main (String[] args) {
        // Logical operators
        boolean x = true;
        boolean y = false;
       
        System.out.println("x && y: " + (x && y));//AND true&&false=false
        System.out.println("x || y: " + (x || y));//OR trur||false=true
        System.out.println("!x: " + (!x));//NOT true return false
}
}
