//Implemets java assignment operators
package Demo;
public class Assignmentopeex 
{

	public static void main(String[] args)
	{
		// Assignment operators
        int f = 7;
        System.out.println("Firstly The value of F is  " + f);
        System.out.println("f += 3: " + (f += 3));//f=7+3//f=10
        System.out.println("f -= 2: " + (f -= 2));//f=10-2=8
        System.out.println("f *= 4: " + (f *= 4));//f=8*4=32
        System.out.println("f /= 3: " + (f /= 3));//f=32/3=10
        System.out.println("f %= 2: " + (f %= 2));//f=10%2=0

	}

}
