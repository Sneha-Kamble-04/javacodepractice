//Primitive Data types : int , char , float, boolean , double.
package Demo;

public class Demo2 
{

	public static void main(String[] args) 
	{
		int num1=10;//Declaration and initialization integer type of variable.
		System.out.println("The value of num1 is = " + num1);
		float num2=12.5f;
		System.out.println("The value of num1 is = " + num2);
		double num3=12.5;
		System.out.println("The value of num1 is = " + num3);
		char c='R';
		System.out.println("The value of Character is = " + c);
		boolean one =true;// in boolean data we can declare values only true and false
		System.out.println("The value of Boolean is  = " + one);
		boolean two = false;// in boolean data we can declare values only true and false
		System.out.println("The value of Boolean is  = " + two);
		
	}

}

